const bodyHtml = `<p>Every August the same two lists make the rounds. One is the &quot;welcome back&quot; collection, all sunshine and fresh-start optimism, perfect for a door sign. The other is the &quot;funny&quot; collection, the one where a quote about coffee and surviving until June gets laminated for the teachers&#39; lounge. Both have their place. A welcome quote sets a tone on day one, and a funny one keeps a faculty meeting from feeling like a sentencing.</p>
<p>But if those are the only two registers you reach for, you are leaving most of the year on the table. A good quote is not just decoration. It is a tiny argument about what school is for, dropped into a kid&#39;s field of vision at a moment when they might actually be listening. So here are seven themes that go past the welcome banner and the laughing-emoji poster, each with lines worth saying out loud and a note on when to use them.</p>
<h2>Courage, not just confidence</h2>
<p>There is a difference between telling students to believe in themselves and telling them the truth: that the important things are scary, and you do them scared. Confidence quotes can ring hollow to a kid who is genuinely nervous about a new grade, a new building, a new social map. Courage quotes meet them where they are.</p>
<blockquote>
<p>&quot;You don&#39;t have to be fearless. You just have to be willing.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;Courage is not the absence of fear. It is the decision that something else matters more.&quot;</p>
</blockquote>
<p>Use these in the first week, when the nerves are real and pretending they are not just makes a student feel alone in them. A courage theme is also a gift to the anxious overachiever, the kid who reads &quot;believe in yourself&quot; as one more performance they might fail. Willingness is a lower, kinder bar than confidence, and it is the one that actually gets people through the door.</p>
<h2>Curiosity over correctness</h2>
<p>School can quietly teach kids that the goal is to be right, and that not knowing is a kind of failure. A whole theme of quotes pushes the other way, toward the idea that the question is the engine and the wrong answer is part of the route.</p>
<blockquote>
<p>&quot;The important thing is not to stop questioning.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;I would rather have questions that cannot be answered than answers that cannot be questioned.&quot;</p>
</blockquote>
<p>This theme earns its keep in science and humanities rooms especially, anywhere you want students raising their hands before they are sure. It also reframes the gradebook in a useful way. If curiosity is the value on the wall, then a half-formed idea offered out loud looks like progress instead of a risk.</p>
<h2>Effort and the long game</h2>
<p>Talent quotes are everywhere, and they are mostly discouraging, because most students have already decided whether they are &quot;a math person&quot; or &quot;a writing person&quot; by the time they reach you. Effort quotes do something better. They argue that the trajectory matters more than the starting point, which happens to be both motivating and true.</p>
<blockquote>
<p>&quot;It is not that I am so smart. It is just that I stay with problems longer.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;Little by little, a little becomes a lot.&quot;</p>
</blockquote>
<p>Save these for the middle stretch, the gray weeks in February when the novelty is gone and the finish line is not in sight. That is exactly when students stop trying because they have confused &quot;this is hard&quot; with &quot;I am bad at this.&quot; A line about persistence on the wall is a quiet rebuttal they can read on their own.</p>
<h2>Belonging and the classroom as a place</h2>
<p>A surprising amount of whether a student learns comes down to whether they feel like they are allowed to be in the room. Belonging quotes are not soft. They are infrastructure. They tell a kid that this space includes them before anyone has earned a grade.</p>
<blockquote>
<p>&quot;You are not a drop in the ocean. You are the entire ocean in a drop.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;Alone we can do so little. Together we can do so much.&quot;</p>
</blockquote>
<p>These are strongest near the door, in the hallway, or printed small on a syllabus where a student rereads it in private. They pair naturally with a class agreement or a set of shared norms, and they do more honest work than a generic &quot;welcome&quot; because they say something specific: you, here, count.</p>
<h2>Failure as information</h2>
<p>Few things shape a learner more than what they have been taught to make of a mistake. If failure means shame, students hide it, and hidden mistakes do not get fixed. A theme built around failure as data turns the worst moment of an assignment into the most useful one.</p>
<blockquote>
<p>&quot;I have not failed. I have just found ten thousand ways that will not work.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;Mistakes are proof that you are trying.&quot;</p>
</blockquote>
<p>Post these near anything high stakes: the exam corner, the lab, the place where drafts get turned in. The point is not to celebrate failure for its own sake, which no student believes anyway. The point is to make the mistake legible, something you read and learn from rather than something you bury.</p>
<h2>Wonder and the world outside the worksheet</h2>
<p>It is easy for school to shrink down to the size of the assignment in front of a student. Wonder quotes pry the windows back open. They remind a class that the worksheet is a doorway to something genuinely large, and that the subject did not begin and will not end with this unit.</p>
<blockquote>
<p>&quot;Look up at the stars and not down at your feet.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;The world is full of magic things, patiently waiting for our senses to grow sharper.&quot;</p>
</blockquote>
<p>These work beautifully at the start of a unit, when you want students to feel the size of what they are about to learn rather than just the weight of it. Wonder is also a quiet antidote to burnout, for teachers as much as kids. A reminder that the material is amazing can carry a room further than a reminder that the test is Friday.</p>
<h2>Integrity and doing the work that is yours</h2>
<p>This is the theme the other lists almost never touch, and it might be the one that matters most right now. In an era when a student can hand an essay to a chatbot in ten seconds, the case for doing your own thinking has to be made on purpose. Integrity quotes make that case without turning it into a threat.</p>
<blockquote>
<p>&quot;Integrity is doing the right thing even when no one is watching.&quot;</p>
</blockquote>
<blockquote>
<p>&quot;The reward of a thing well done is having done it.&quot;</p>
</blockquote>
<p>Put these where the work happens, and let them do something a plagiarism policy cannot. A rule tells students what they are not allowed to do. A line like &quot;the reward of a thing well done is having done it&quot; tells them why they would want to do it anyway, which is the only version that survives contact with a deadline at midnight. The goal of a tool like ours is to protect that effort, but the better outcome is a student who values their own thinking enough that the tool rarely has anything to flag.</p>
<h2>How to actually use these</h2>
<p>A quote does not work because it is profound. It works because it is timed. The welcome line lands on day one and goes stale by October. A courage line is wasted in a calm week and priceless before a first presentation. So treat your quotes like a playlist rather than a poster: pick the theme that fits the moment your students are actually in, rotate them, and say a few out loud instead of letting them all fade into the wallpaper.</p>
<p>And the next time you reach for &quot;welcome back&quot; or a coffee joke, ask which of these seven the room actually needs. The best back-to-school quote is not the cleverest one. It is the one a kid happens to read on the morning they needed exactly that.</p>`;

